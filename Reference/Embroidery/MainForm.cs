        private void MainForm_Load(object sender, EventArgs e)
        {
            
            ftv.Width = LeftSplitter.Width;
            ftv.Height =400;
            ftv.InitFolderTreeView();
            ftv.Anchor = AnchorStyles.Top|AnchorStyles.Right;

            tab.TabPages["tabfile"].Controls.Add(ftv);
            ftv.AfterSelect += new TreeViewEventHandler(ftv_AfterSelect);
        }

        void ftv_AfterSelect(object sender, TreeViewEventArgs e)
        {
            if (ftv.GetSelectedNodePath()!="")
            {
                if (eb == null)
                    eb = new EmbroideryBrowser();
                eb.MdiParent = this;
                eb.Show();
                eb.LoadPath(ftv.GetSelectedNodePath());
               
            }
        }
